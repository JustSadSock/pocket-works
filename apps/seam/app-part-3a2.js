function caption() {
  if (game.winner) return '\u0414\u0443\u044d\u043b\u044c \u043e\u043a\u043e\u043d\u0447\u0435\u043d\u0430';
  if (aiBusy) return THINKING[aiStyle];
  if (!humanCanAct()) return '\u0416\u0434\u0451\u043c \u0441\u043e\u043f\u0435\u0440\u043d\u0438\u043a\u0430';
  if (deployMode) {
    return game.deploymentCells(game.turn).length
      ? '\u041f\u043e\u0434\u043a\u0440\u0435\u043f\u043b\u0435\u043d\u0438\u0435: \u0432\u044b\u0431\u0435\u0440\u0438\u0442\u0435 \u043a\u043b\u0435\u0442\u043a\u0443 \u0434\u043e\u043c\u0430\u0448\u043d\u0435\u0433\u043e \u043a\u0440\u0430\u044f'
      : '\u041d\u0435\u0442 \u043f\u043e\u0434\u0434\u0435\u0440\u0436\u0430\u043d\u043d\u043e\u0439 \u043a\u043b\u0435\u0442\u043a\u0438 \u0434\u043b\u044f \u0432\u043e\u0437\u0432\u0440\u0430\u0442\u0430';
  }
  if (game.centerClaim) {
    const ours = mode === 'local' || seatForColor[game.centerClaim.player] === 1;
    return `${ours ? '\u0423\u0434\u0435\u0440\u0436\u0438\u0442\u0435' : '\u0421\u043e\u0440\u0432\u0438\u0442\u0435'} \u041e\u0441\u044c: ${game.centerClaim.replies}/${game.centerReplies}`;
  }
  if (!selected.length) return '\u0412\u044b\u0431\u0435\u0440\u0438\u0442\u0435 \u0441\u0432\u043e\u044e \u0444\u0438\u0448\u043a\u0443';
  if (selected.length === 1 && selected[0] === game.crown[game.turn]) {
    return '\u041a\u043e\u0440\u0435\u043d\u044c \u043d\u0435 \u0445\u043e\u0434\u0438\u0442 \u043e\u0434\u0438\u043d \u2014 \u0434\u043e\u0431\u0430\u0432\u044c\u0442\u0435 \u0441\u043e\u044e\u0437\u043d\u0438\u043a\u0430 \u0432 \u043b\u0438\u043d\u0438\u044e';
  }
  const legal = game.legalMovesForSelection(selected.map(parseCoordKey));
  return legal.length
    ? `\u0421\u0442\u0440\u043e\u0439 \u0438\u0437 ${selected.length}: \u043d\u0430\u0436\u043c\u0438\u0442\u0435 \u0441\u0442\u0440\u0435\u043b\u043a\u0443`
    : '\u0414\u043e\u0431\u0430\u0432\u044c\u0442\u0435 \u0441\u043e\u0441\u0435\u0434\u043d\u044e\u044e \u0444\u0438\u0448\u043a\u0443 \u043f\u043e \u043f\u0440\u044f\u043c\u043e\u0439';
}

